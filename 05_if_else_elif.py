# The simplest flow control statements are If aand Else

name = 'Alice'
if name == 'Alice':
    print('Hi Alice')
print('Done')

# The identation is how python tells what part is inside the IF statements block and what isn't. 
# A block begins when the identation increases and ends when the identation returns to its previous level.

# Blocks are also called clauses 
# New blocks begin only after statements that end with a colon like in the IF statement

# else statement
# With the if else statements one of two blocks is executed

password = 'swordfish'
if password == 'swordfish':
    print('Access granted.')
else:
    print('Wrong password.')

# else if statement
# One of many possible blocks to execute the if statement 

